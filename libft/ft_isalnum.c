#include "libft.h"

int	ft_isalnum(int nc)
{
	if ((nc >= '0' && nc <= '9') || (nc >= 'a' && nc <= 'z')
		|| (nc >= 'A' && nc <= 'Z'))
		return (1);
	else
		return (0);
}
